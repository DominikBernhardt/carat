/**************************************************************************\
@--------------------------------------------------------------------------
@
@int KKGV(list, list_anz)
@     int *list;
@     int list_anz;
@
@calculates the kgv (smallest common multiple) of a list of integers.
@
@--------------------------------------------------------------------------
\**************************************************************************/
int KKGV(list, list_anz)
     int *list;
     int list_anz;
{
  int ggt,kgv;
  int i;
  extern int GGT();
  
  kgv = list[0];
  
  for(i=1;i<list_anz; i++)
    {
      ggt = GGT(kgv,list[i]);
      
      if(kgv >= list[i])
 	kgv = (kgv/ggt)*list[i]; 
      else
 	kgv = (list[i]/ggt)*kgv;
    }
  return (kgv);
}

